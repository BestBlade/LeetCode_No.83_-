#include <iostream>
#include <string> 
#include <vector>
#include <algorithm>
#include <unordered_map>
#include <map>
#include <stack>

using namespace std;

struct ListNode {
    int val;
    ListNode* next;
    ListNode(int x) : val(x), next(NULL) {}
    
};

struct TreeNode {
    int val;
    TreeNode* left;
    TreeNode* right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

ListNode* deleteDuplicates(ListNode* head) {
    ListNode* node = head;

    while (node && node->next) {
        if (node->val == node->next->val) {
            node->next = node->next->next;
        }
        else {
            node = node->next;
        }
    }
    return head;
}